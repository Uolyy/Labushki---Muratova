# 4.3)	Напишите функцию, которая возвращает True,
# если введенная пользователем дата является магической, и False в обратном случае.
# Магической считается дата, в которой произведение дня и месяца равно двум последним цифрам года, например: 02.11.2022.

def magic():
    try:
        day, month, year = int(input("Введите день: ")), int(input("Введите месяц: ")), input("Введите год: ")
        # преобразование отдельных переменных в одну красивенькую дату
        date_str = f"{day:02d}.{month:02d}"
        # нагугленная некоторая магия, проверяющая корректность даты (ОНО РАБОТАЕТ!! САМО!!11!!1!)
        datetime.datetime.strptime(date_str, "%d.%m")
        if day * month == int(year[-2:]):
            return True
        else:
            return False
    except ValueError:
        print("Некорректная дата")
        return False


