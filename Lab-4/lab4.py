# 4.1)	Напишите функцию, которая проверяет, делится ли введенное число на 3, или нет.

def division_3():
    x = int(input("Введите проверяемое число: "))
    if x % 3 == 0:
        print(f"Число {x} делится на 3")
    else:
        print(f"Число {x} не делится на 3")


